package com.example.dailybite;

public class NutrientData {
    float proteins;
    float fats;
    float carbs;
    float calories;

    public NutrientData(float proteins, float fats, float carbs, float calories) {
        this.proteins = proteins;
        this.fats = fats;
        this.carbs = carbs;
        this.calories = calories;
    }
}
